/* Area of Circle */
#include <stdio.h>

int main()
{
    float A, r= 2;
    float Pi=3.14;
    A = Pi * r * r;

    printf("The Area of Circle is A=: %.2f", A);

    return 0;
}

/* Volume of Cylinder */
#include <stdio.h>

int main()
{
    float V, r = 5, h = 10;
    float Pi = 3.14;
    V = Pi * r * r * h;

    printf("The volume of cylinder V is =: %.2f", V);

    return 0;

}
